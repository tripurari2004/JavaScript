console.log(name > name2);  // Output: false (because "Alice" comes before "Bob" in alphabetical order)
console.log(name <= name2); // Output: true (because "Alice" comes before "Bob" in alphabetical order)
console.log(name >= name2); // Output: false (because "Alice" comes before "Bob" in alphabetical order)


